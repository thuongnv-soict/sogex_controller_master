class Cluster:
    def __init__(self, id, name, space, number_of_accounts, number_of_servers, number_of_followings):
        self.id = id
        self.name = name
        self.space = space
        self.number_of_accounts = number_of_accounts
        self.number_of_servers = number_of_servers
        self.number_of_followings = number_of_followings